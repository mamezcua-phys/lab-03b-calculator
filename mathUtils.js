// Declaring a function to add numbers
export function addTwoNumbers(number1, number2) {
     return number1 + number2;
}

export function subtractTwoNumbers(number1, number2) {
     return number1 - number2;
}

export function multiplyTwoNumbers(number1, number2) {
     return number1 * number2;
}

export function divideTwoNumbers(number1, number2) {
     return number1 / number2;
}